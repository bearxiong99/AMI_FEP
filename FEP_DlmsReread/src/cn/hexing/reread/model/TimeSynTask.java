package cn.hexing.reread.model;
/**
 * 自动对时任务
 * @ClassName:TimeSynTask
 * @Description:TODO
 * @author kexl
 * @date 2012-11-15 下午02:41:23
 *
 */
public class TimeSynTask {
	private String dwdm; //单位代码
	
	private String rwlx; //任务类型
	
	private String zxsj; //执行时间
	
	private String dqzt; //读取状态

	public TimeSynTask() {
		super();
	}

	public TimeSynTask(String dwdm, String rwlx, String zxsj, String dqzt) {
		super();
		this.dwdm = dwdm;
		this.rwlx = rwlx;
		this.zxsj = zxsj;
		this.dqzt = dqzt;
	}

	public String getDwdm() {
		return dwdm;
	}

	public void setDwdm(String dwdm) {
		this.dwdm = dwdm;
	}

	public String getRwlx() {
		return rwlx;
	}

	public void setRwlx(String rwlx) {
		this.rwlx = rwlx;
	}

	public String getZxsj() {
		return zxsj;
	}

	public void setZxsj(String zxsj) {
		this.zxsj = zxsj;
	}

	public String getDqzt() {
		return dqzt;
	}

	public void setDqzt(String dqzt) {
		this.dqzt = dqzt;
	}

	public String toString() {
		return "{dwdm="+dwdm+",rwlx="+rwlx+",zxsj="+zxsj+",dqzt="+dqzt+"}";
	}
}
